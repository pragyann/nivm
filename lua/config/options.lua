local set = vim.opt

-- line nums
set.number = true
set.relativenumber = true

-- indentation and tabs
set.tabstop = 4
set.shiftwidth = 4
set.autoindent = true
set.expandtab = true

-- search settings
set.ignorecase = true
set.smartcase = true

-- appearance
set.background = "dark"
set.signcolumn = "yes"
set.termguicolors = true

-- cursor line
set.cursorline = true

-- split windows
set.splitbelow = true
set.splitright= true

-- faster cursor hold
set.updatetime = 50

